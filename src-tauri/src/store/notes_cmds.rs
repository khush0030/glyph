/// Replace all transcript segments for a note (called after a recording stops).
#[tauri::command]
pub fn save_segments(db: State<'_, Db>, note_id: String, segments: Vec<SegmentIn>) -> Result<(), String> {
    let mut conn = db.0.lock().map_err(|e| e.to_string())?;
    let tx = conn.transaction().map_err(|e| e.to_string())?;
    // Safety net: never lose a recording because the note row is missing
    // (deleted, or createNote failed). Recreate it idempotently.
    tx.execute(
        "INSERT OR IGNORE INTO notes (id, title, created_at, updated_at, source, status) VALUES (?1, '', ?2, ?2, 'recorded', 'draft')",
        params![note_id, now_ms()],
    )
    .map_err(|e| e.to_string())?;
    tx.execute("DELETE FROM segments WHERE note_id = ?1", params![note_id])
        .map_err(|e| e.to_string())?;
    for (i, s) in segments.iter().enumerate() {
        tx.execute(
            "INSERT INTO segments (id, note_id, idx, start_ms, end_ms, text, lang) VALUES (?1,?2,?3,?4,?5,?6,?7)",
            params![new_id(), note_id, i as i64, s.start_ms, s.end_ms, s.text, s.lang],
        )
        .map_err(|e| e.to_string())?;
    }
    touch(&tx, &note_id);
    tx.commit().map_err(|e| e.to_string())?;
    export_transcript(&conn, &note_id, &segments);
    Ok(())
}

/// Persist the generated note + its AI action items (replacing prior AI items).
#[tauri::command]
#[allow(clippy::too_many_arguments)]
pub fn save_generated(
    db: State<'_, Db>,
    note_id: String,
    summary: String,
    key_points: Vec<String>,
    decisions: Vec<String>,
    open_questions: Vec<String>,
    action_items: Vec<ActionItemIn>,
    model: String,
) -> Result<(), String> {
    let mut conn = db.0.lock().map_err(|e| e.to_string())?;
    let json = serde_json::json!({
        "summary": summary, "keyPoints": key_points, "decisions": decisions,
        "openQuestions": open_questions, "model": model
    })
    .to_string();
    let tx = conn.transaction().map_err(|e| e.to_string())?;
    tx.execute(
        "INSERT OR IGNORE INTO notes (id, title, created_at, updated_at, source, status) VALUES (?1, '', ?2, ?2, 'recorded', 'draft')",
        params![note_id, now_ms()],
    )
    .map_err(|e| e.to_string())?;
    tx.execute("DELETE FROM generated_notes WHERE note_id = ?1", params![note_id])
        .map_err(|e| e.to_string())?;
    tx.execute(
        "INSERT INTO generated_notes (note_id, markdown, model, generated_at) VALUES (?1,?2,?3,?4)",
        params![note_id, json, model, now_ms()],
    )
    .map_err(|e| e.to_string())?;
    tx.execute("DELETE FROM action_items WHERE note_id = ?1 AND source = 'ai'", params![note_id])
        .map_err(|e| e.to_string())?;
    for a in &action_items {
        tx.execute(
            "INSERT INTO action_items (id, note_id, text, assignee, due_hint, source) VALUES (?1,?2,?3,?4,?5,'ai')",
            params![new_id(), note_id, a.text, a.assignee, a.due_hint],
        )
        .map_err(|e| e.to_string())?;
    }
    tx.execute("UPDATE notes SET status = 'ready', updated_at = ?2 WHERE id = ?1", params![note_id, now_ms()])
        .map_err(|e| e.to_string())?;
    tx.commit().map_err(|e| e.to_string())?;
    export_notes_md(&conn, &note_id, &summary, &key_points, &decisions, &open_questions, &action_items, &model);
    Ok(())
}

/// A filesystem-safe slug from a meeting title (for PDF filenames).
fn slugify(title: &str) -> String {
    let s: String = title
        .trim()
        .chars()
        .map(|c| if c.is_alphanumeric() { c.to_ascii_lowercase() } else { '-' })
        .collect();
    let s = s.trim_matches('-').replace("--", "-");
    let s: String = s.chars().take(60).collect();
    if s.trim_matches('-').is_empty() { "meeting".into() } else { s }
}

/// Write a PDF (built in the webview, passed as standard base64) into the
/// meeting folder and open it. Returns the file path.
#[tauri::command]
pub fn save_note_pdf(db: State<'_, Db>, note_id: String, pdf_base64: String) -> Result<String, String> {
    let title: String = {
        let conn = db.0.lock().map_err(|e| e.to_string())?;
        conn.query_row("SELECT title FROM notes WHERE id = ?1", params![note_id], |r| r.get(0))
            .unwrap_or_default()
    };
    let bytes = base64::engine::general_purpose::STANDARD
        .decode(pdf_base64.trim())
        .map_err(|e| format!("bad PDF data: {e}"))?;
    let dir = meeting_dir(&note_id).ok_or("no home directory")?;
    std::fs::create_dir_all(&dir).map_err(|e| e.to_string())?;
    let path = dir.join(format!("{}-notes.pdf", slugify(&title)));
    std::fs::write(&path, bytes).map_err(|e| e.to_string())?;
    let _ = std::process::Command::new("open").arg(&path).spawn();
    Ok(path.to_string_lossy().into_owned())
}

/// Open the meeting's local-files folder (transcript.txt + notes.md) in Finder.
#[tauri::command]
pub fn reveal_note_files(id: String) -> Result<String, String> {
    let dir = meeting_dir(&id).ok_or("no home directory")?;
    std::fs::create_dir_all(&dir).map_err(|e| e.to_string())?;
    std::process::Command::new("open")
        .arg(&dir)
        .spawn()
        .map_err(|e| e.to_string())?;
    Ok(dir.to_string_lossy().into_owned())
}

